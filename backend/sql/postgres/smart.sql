-- =================================================================
-- 创建知识库文件表 (knowledge_file)
-- 用途：存储文件/文档元数据，不存content，content+向量存在 PGVector 中
-- =================================================================
-- 设计思路:
-- 一个用户上传的文件 → 切分为N个chunk → 每个chunk存入PGVector
-- KnowledgeFile 只存这个文件的**元信息**，方便管理和列表展示
-- =================================================================
DROP TABLE IF EXISTS public.knowledge_file CASCADE;
CREATE TABLE public.knowledge_file (
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    uuid character varying(64) NOT NULL DEFAULT gen_random_uuid()::text,
    knowledge_base_id integer NOT NULL,
    file_name character varying(256) NOT NULL,
    description text,
    file_size bigint,
    chunk_count integer NOT NULL DEFAULT 0,
    source character varying(512),
    status integer NOT NULL DEFAULT 0,
    is_deleted boolean NOT NULL DEFAULT false,
    created_time timestamp without time zone NOT NULL DEFAULT NOW(),
    updated_time timestamp without time zone NOT NULL DEFAULT NOW(),
    created_id integer,
    updated_id integer,
    CONSTRAINT pk_knowledge_file PRIMARY KEY (id),
    CONSTRAINT uk_knowledge_file_uuid UNIQUE (uuid)
);

COMMENT ON TABLE public.knowledge_file IS '知识库文件表 - 存储文件/文档元数据（content不在这里存储）';
COMMENT ON COLUMN public.knowledge_file.id IS '主键ID';
COMMENT ON COLUMN public.knowledge_file.uuid IS '文件UUID';
COMMENT ON COLUMN public.knowledge_file.knowledge_base_id IS '所属知识库ID';
COMMENT ON COLUMN public.knowledge_file.file_name IS '文件名/文档标题';
COMMENT ON COLUMN public.knowledge_file.description IS '文件描述';
COMMENT ON COLUMN public.knowledge_file.file_size IS '原始文件大小(字节)';
COMMENT ON COLUMN public.knowledge_file.chunk_count IS '切片数量';
COMMENT ON COLUMN public.knowledge_file.source IS '文档来源(文件路径/URL)';
COMMENT ON COLUMN public.knowledge_file.status IS '状态: 0-处理中 1-成功 2-失败';
COMMENT ON COLUMN public.knowledge_file.is_deleted IS '是否软删除';
COMMENT ON COLUMN public.knowledge_file.created_time IS '创建时间';
COMMENT ON COLUMN public.knowledge_file.updated_time IS '更新时间';
COMMENT ON COLUMN public.knowledge_file.created_id IS '创建人ID';
COMMENT ON COLUMN public.knowledge_file.updated_id IS '更新人ID';

-- 创建索引
CREATE INDEX idx_knowledge_file_knowledge_base_id ON public.knowledge_file USING btree (knowledge_base_id);
CREATE INDEX idx_knowledge_file_status ON public.knowledge_file USING btree (status);
CREATE INDEX idx_knowledge_file_is_deleted ON public.knowledge_file USING btree (is_deleted);

-- 创建触发器 (自动更新 updated_time)
CREATE TRIGGER update_knowledge_file_updated_time
BEFORE UPDATE ON public.knowledge_file
FOR EACH ROW
EXECUTE FUNCTION public.update_updated_time();

