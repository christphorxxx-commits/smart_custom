-- =================================================================
-- PostgreSQL 数据库初始化脚本 (优化版)
-- 模块：用户与角色核心管理
-- 版本：2.0.3-Fixed
-- 说明：修复了主键缺失、Owner 硬编码、扩展依赖及幂等性问题，调整了UUID字段命名
