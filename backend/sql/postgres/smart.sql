-- =================================================================
-- 创建 AI应用表 (app)
-- 用途：存储用户创建的AI应用/工作流基本信息和权限控制
-- 完整工作流配置存储在 MongoDB apps 集合中
-- =================================================================
CREATE TABLE public.app (
    id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    name character varying(200) NOT NULL,
    app_id character varying(64) NOT NULL DEFAULT gen_random_uuid()::text,
    user_id integer NOT NULL,
    description text,
    icon character varying(20) DEFAULT '🤖',
    is_public boolean NOT NULL DEFAULT false,
    type character varying(20) NOT NULL DEFAULT 'workflow',
    status character varying(10) NOT NULL DEFAULT '0',
    created_time timestamp without time zone NOT NULL DEFAULT NOW(),
    updated_time timestamp without time zone NOT NULL DEFAULT NOW(),
    CONSTRAINT pk_app PRIMARY KEY (id),
    CONSTRAINT uk_app_app_id UNIQUE (app_id)
);

COMMENT ON TABLE public.app IS 'AI应用表 - 存储应用基本信息和权限';
COMMENT ON COLUMN public.app.id IS '主键ID';
COMMENT ON COLUMN public.app.name IS '应用名称';
COMMENT ON COLUMN public.app.app_id IS '应用唯一标识，对应MongoDB App.app_id';
COMMENT ON COLUMN public.app.user_id IS '创建用户ID (权限控制)';
COMMENT ON COLUMN public.app.description IS '应用描述';
COMMENT ON COLUMN public.app.icon IS '应用图标emoji';
COMMENT ON COLUMN public.app.is_public IS '是否公开分享';
COMMENT ON COLUMN public.app.type IS '应用类型: workflow/ai/chat';
COMMENT ON COLUMN public.app.status IS '是否启用(0:启用 1:禁用)';
COMMENT ON COLUMN public.app.created_time IS '创建时间';
COMMENT ON COLUMN public.app.updated_time IS '更新时间';

-- 创建索引
CREATE INDEX idx_app_user_id ON public.app USING btree (user_id);
CREATE INDEX idx_app_status ON public.app USING btree (status);
CREATE INDEX idx_app_type ON public.app USING btree (type);
CREATE INDEX idx_app_is_public ON public.app USING btree (is_public);

-- 创建触发器 (自动更新 updated_time)
CREATE TRIGGER update_app_updated_time
BEFORE UPDATE ON public.app
