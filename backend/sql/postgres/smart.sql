-- 0. 先回滚任何已失败的事务，确保干净的开始
ROLLBACK;

-- 1. 开启事务，确保原子性
BEGIN;

-- 2. 设置环境参数
SET statement_timeout = 0;
SET lock_timeout = 0;
SET idle_in_transaction_session_timeout = 0;
SET transaction_timeout = 0;
SET client_encoding = 'UTF8';
SET standard_conforming_strings = on;
SELECT pg_catalog.set_config('search_path', '', false);
SET check_function_bodies = false;
SET xmloption = content;
SET client_min_messages = warning;
SET row_security = off;

SET default_tablespace = '';
SET default_table_access_method = heap;

-- 3. 确保 UUID 扩展存在 (gen_random_uuid() 需要 pgcrypto)
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- =================================================================
-- 清理旧对象 (支持重复执行脚本)
-- 注意：生产环境请谨慎使用 DROP CASCADE，它会删除依赖此表的外键
-- =================================================================
DROP TABLE IF EXISTS public.sys_user_roles CASCADE;
DROP TABLE IF EXISTS public.sys_user CASCADE;
DROP TABLE IF EXISTS public.sys_role CASCADE;
DROP TABLE IF EXISTS public.app CASCADE;
DROP FUNCTION IF EXISTS public.update_updated_time() CASCADE;

-- =================================================================
-- 创建公共函数：自动更新 updated_time
-- =================================================================
CREATE OR REPLACE FUNCTION public.update_updated_time()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_time = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

